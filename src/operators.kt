fun main() {
    //arithmetic operators
    var x = 10
    var y = 3

    println(x+y) //sum
    println(x-y) //subtraction
    println(x*y) //multiplication
    println(x/y) //division
    println(x%y) //modulus

    //Comparison operators
    var num1 = 45
    var num2 = 30

    println(num1 < num2)
    println(num1 > num2)
    println(num1 <= num2)
    println(num1 >= num2)
    println(num1 == num2)
    println(num1 != num2)//not equal to

    //Assignment operators
    var z = 56
    println(z)

    var number = 10
    number += 2
    println(number)

    //Logic operators - &&, ||, !
    println(67 > 12 && 80 < 6)
    println(67 > 12 || 80 < 6)
    println(!(67 > 12 || 80 < 6))


}